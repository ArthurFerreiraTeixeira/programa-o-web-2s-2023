var numA = 10;
var numB = 5;

// Soma
var sumResult = numA + numB;
console.log(`Resultado da soma entre ${numA} e ${numB} = ${sumResult}`);

// Subtração
var subtractResult = numA - numB;
console.log(`Resultado da subtração entre ${numA} e ${numB} = ${subtractResult}`);

// Multiplicação
var multiplyResult = numA * numB;
console.log(`Resultado da multiplicação entre ${numA} e ${numB} = ${multiplyResult}`);

// Divisão
var divideResult = numA / numB;
console.log(`Resultado da divisão entre ${numA} e ${numB} = ${divideResult}`);

// Resto da divisão (Módulo)
var moduloResult = numA % numB;
console.log(`Resultado do resto da divisão entre ${numA} e ${numB} = ${moduloResult}`);